// Use a `while loop` to count to the number 20
 
// using a while loop to count to 20 is very much similar to using a for loop to count to 20

// the only thing that is a little different is that we initialize out of the while loop parentheses like so `var i = 0`

// inside of the parentheses we make our condition
// then we log `i` and lastly we increment by 1 each time our loop counts up. Incrementing outside of the parentheses allows for the while loop to break. 
var i = 0;
while (i < 21){
  console.log(i);
  i++;
}